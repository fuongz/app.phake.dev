import { Menu, Transition } from "@headlessui/react";
import { Fragment } from "react";
import { ChevronDownIcon } from "@heroicons/react/solid";
import Link from "next/link";

export default function Dropdown(props: any) {
  return (
    <div className="-ml-4 text-right">
      <Menu as="div" className="relative inline-block text-left">
        <div>
          <Menu.Button className="inline-flex justify-center w-full pl-8 pr-4 py-2 text-sm font-medium text-gray-100 bg-gray-500 rounded-md bg-opacity-20 hover:bg-opacity-30 focus:outline-none focus-visible:ring-2 focus-visible:ring-white focus-visible:ring-opacity-75">
            {props.label}
            <ChevronDownIcon
              className="w-5 h-5 ml-2 -mr-1 text-gray-100"
              aria-hidden="true"
            />
          </Menu.Button>
        </div>
        <Transition
          as={Fragment}
          enter="transition ease-out duration-100"
          enterFrom="transform opacity-0 scale-95"
          enterTo="transform opacity-100 scale-100"
          leave="transition ease-in duration-75"
          leaveFrom="transform opacity-100 scale-100"
          leaveTo="transform opacity-0 scale-95"
        >
          <Menu.Items className="absolute py-2 right-0 w-40 mt-2 origin-top-right bg-gray-700 divide-y divide-gray-600 rounded-md shadow-lg ring-1 ring-black ring-opacity-5 focus:outline-none">
            {props.items &&
              props.items.map((item: any, index: any) => (
                <Menu.Item key={`dropdown-${index}`}>
                  {({ active }) =>
                    item.to ? (
                      <Link href={item.to}>
                        <a
                          className={`group flex items-center w-full px-3 py-2 text-sm text-gray-300 hover:bg-blue-600 hover:text-gray-100`}
                        >
                          {active}
                          {item.label}
                        </a>
                      </Link>
                    ) : (
                      <span
                        className={`${
                          active ? "bg-blue-600 text-gray-100" : "text-gray-300"
                        } group flex items-center w-full px-3 py-2 text-sm`}
                        onClick={(e) => {
                          e.preventDefault();
                          e.stopPropagation();
                          return item.onClick ? item.onClick(e) : null;
                        }}
                      >
                        {item.label}
                      </span>
                    )
                  }
                </Menu.Item>
              ))}
          </Menu.Items>
        </Transition>
      </Menu>
    </div>
  );
}

